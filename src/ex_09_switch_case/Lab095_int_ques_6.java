package ex_09_switch_case;

public class Lab095_int_ques_6
{
    public static void main(String[] args)
    {
            int a = 11;

            switch (-1)
            {
                default :
                    System.out.println("Default");
                    break;
                case -1 :
                    System.out.println("10");
                    break;
                case 9:
                    System.out.println("9");
                    break;
            }
    }
}
